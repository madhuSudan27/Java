package OOPS;

class Student {
    int age;
    String name;
    int regNumber;
    String branch;

    Student(int age , String name, int regNumber ){
        this.name = name;
        this.age = age;
        this.regNumber = regNumber;
    }

    Student(int age , String name, int regNumber , String branch){
        this.name = name;
        this.age = age;
        this.regNumber = regNumber;
        this.branch = branch;
    }
    // lets define a setter and getter

}
